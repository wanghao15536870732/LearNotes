//二叉树的查找
bintnode *locate(bintnode *t,int x)
{
	bintnode *p;
	if(t == NULL)
		return NULL;
	else if(t->data == x)
		return t;
	else
	{
		p = locate(t->lchild,x);
		if(p)
			return p;
		else
			return locate(t->rchild,x); 
	}
}

//递归统计二叉树中的节点个数
int numofnode(bintnode *t)
{
	if(t == NULL)
		return 0;
	else
		return(numofnode(t->lchild) + numofnode(t->rchild) + 1);
}

//非递归统计二叉树中的节点个数
int numofleaf(bintnode *t)
{
	int count = 0;
	seqstack s;
	s.top = 0;
	while((t) || (s.top != 0))
	{
		if(t)
		{
			count ++;
			push(&s,t);
			t = t->lchild;
		}
		else
		{
			t = pop(&s);
			t = t->rchild;
		}
	}
	return count;
} 

//判断二叉树是否等价
int isequal(bintnode *t1,bintnode *t2)
{
	int t = 0;
	if(t1 == NULL && t2 == NULL)
		t = 1;
	else if(t1 != NULL && t2 != NULL)
		if(t1->data == t2->data)
			if(isequal(t1->lchild,t1->rchild))
				t = isequal(t1->lchild,t2->rchild);
	return (t);
}

